<p><strong>2. Tháo bao cao su quá sớm</strong></p>

<p>Có 13,6% đến 44,7% người tham gia trả lời tháo bao su ở gần cuối cuộc vui, sau đó vẫn quan hệ và có tiếp xúc tình dục xâm nhập. Hãy nhớ rằng bao cao su phải được mang trong suốt thời gian quan hệ.</p>

<p><strong>3. Duỗi bao cao su ra trước khi mang vào</strong></p>

<p>Thay vì úp bao cao su lên đầu "cậu nhỏ" và tuột dần xuống tận gốc, một số người lại chọn cách vuốt hẳn bao ra rồi mới mang vào. Bằng cách này, vô tình làm giãn bao cao su và khiến nó không còn ôm sát vào thân "cậu nhỏ" nữa. Có đến 25,3% người tham gia lại thừa nhận áp dụng cách đeo bao cao su như vậy.</p>

<p>Cách làm đúng trong trường hợp này là: Úp bao lên đầu dương vật, sau đó vuốt xuống dần, bao cao su sẽ tuột dần đến gốc.</p>

<p><strong>4. Không chừa trống ở phía đầu để chứa tinh dịch</strong></p>

<p>Có đến 45,7% người tham gia thừa nhận từng thất bại trong việc không chừa đủ chỗ ở đầu bao cao su để chứa tinh dịch. Trong một số tình huống, túi chứa tinh ở đầu bao có thể quá nhỏ khiến tinh dịch trào ra ngoài khi xuất tinh.</p>

<p>Thông thường, ở đầu bao cao su có túi chứa tinh nhỏ. Khi đeo, không nên vuột quá mạnh xuống, khiến cho phần đầu bao (bao gồm túi chứa tinh và phần trống phía đầu) bị căng và giảm thể tích.</p>

<p><strong>5. Không bóp xẹp đầu bao chứa tinh dịch</strong></p>

<p>Khi được hỏi về lần quan hệ tình dục cuối, 48,1% nữ giới và 41,6% nam giới cho biết không bóp xẹp đầu bao cao su trước khi đeo. Việc còn lưu khí trong đầu chứa tinh có thể khiến bao bị vỡ hay rách khi đè nén trong quan hệ.</p>

<p>Lưu ý bạn cách dùng bao cao su an toàn như sau:</p>

<p>- Kiểm tra thời hạn sử dụng.</p>

<p>- Dồn bao cao su về một phía, xé vỏ bao theo đường răng cưa có sẵn.</p>

<p>- Dùng hai ngón tay (ngón cái và ngón trỏ) bóp xẹp hoặc xoắn nhẹ đầu bao cao su cho không khí thoát hết ra ngoài. Sau đó úp bao lên đầu dương vật (chú đặt ý vòng cuốn bao cao su ở phía bên ngoài) và tuốt bao xuống hết chiều dài. Bước này nhiều người thường không tuân thủ cẩn thận nên dễ làm rách bao khi quan hệ.</p>

<p><strong>6. Lộn ngược bao cao su khi đeo</strong></p>

<p>Từ 4 đến 30,4% cho biết họ từng đeo ngược bao cao su. Khi phát hiện, họ lộn ngược bao và tiếp tục đeo.</p>

<p>Cách làm đúng, nếu đeo ngược bao cao su (khi vuột bao xuống sẽ thấy có trở ngại), bạn nên thay bằng một chiếc bao khác.</p>

<p><strong>7. Không tuột hết bao cao su khi đeo</strong></p>

<p>Khi chia sẻ về lần quan hệ cuối, 11,2% nữ giới và 8,8% nam giới không tuột hết bao cao su trước khi bắt đầu quan hệ.</p>

<p>Cách làm đúng: Phải tuột hết bao cao su cho đến khi lộ ra vòng gân của bao. Nếu không, bao có thể bị tuột trong lúc quan hệ.</p>

<p><strong>8. Mở bao cao su bằng vật nhọn</strong></p>

<p>Có 2,1 đến 11,2% cho biết họ thường mở vỏ bao bằng vật sắc nhọn.</p>

<p>Cách làm đúng: Không dùng vật sắc nhọn để xé vỏ bao. Khi xé, có thể đẩy bao cao su sang bên rồi xé nhẹ ở một góc theo đường răng cưa trên vỏ.</p>

<p><strong>9. Không kiểm tra bao</strong></p>

<p>Khi sử dụng bao cao su, 82,7% nữ giới và 74,5% nam giới cho biết họ không kiểm tra bao trước khi sử dụng.</p>

<p>Cách làm đúng:</p>

<p>- Phải kiểm tra hạn sử dụng.</p>

<p>- Quan sát vỏ bao còn mới hay chưa bị rách. Nếu trong bóng tối, có thể bóp nhẹ lên vỏ bao, nếu có cảm giác còn có “không khí” chứng tỏ vỏ bao còn kín, nếu không tức là vỏ bao đã bị xì, không khí bên ngoài đã vào và có thể làm hư bao.</p>

<p>- Khi đeo vào nên cẩn thận quan sát xem bao có bị rách hay thủng không.</p>

<p><strong>10. Không sử dụng chất bôi trơn</strong></p>

<p>16 đến 25,8% cho biết không sử dụng kèm chất bôi trơn khi đeo bao cao su.</p>

<p>Cách làm đúng: Cần sử dụng chất bôi trơn, nhất là khi âm đạo không tiết đủ chất nhờn, hay khi quan hệ kéo dài. Nam đồng giới phải sử dụng chất bôi trơn khi quan hệ qua ngả hậu môn.</p>

<p><strong>11. Sử dụng chất bôi trơn gốc dầu</strong></p>

<p>Vẫn có 3,2 đến 4,7% cho biết sử dụng chất bôi trơn gốc dầu, vốn không tương thích với bao cao su. Đúng ra phải sử dụng chất bôi trơn gốc nước.</p>

<p><strong>12. Rút cậu nhỏ không kịp thời</strong></p>

<p>Gần 31% nam giới và 27% nữ giới cho biết họ không rút "cậu nhỏ" ra kịp thời và đúng cách sau khi người nam đã xuất tinh.</p>

<p>Cách làm đúng: Sau khi xuất tinh, "cậu nhỏ" sẽ nhanh chóng xìu xuống. Vì vậy, nam giới cần chủ động rút ra khỏi "cô bé" để tránh bao cao su bị tuột hay tinh dịch tràn ra, làm giảm hiệu quả của công cụ bảo vệ.</p>

<p><strong>13. Tái sử dụng bao cao su</strong></p>

<p>Dù rất ít, vẫn có 1,4 đến 3,3% người cho biết họ sử dụng lại bao cao su.</p>

<p>Cách làm đúng: Bao cao su chỉ được sử dụng một lần, cho một người duy nhất.</p>

<p><strong>14. Bảo quản sai cách</strong></p>

<p>3,3 đến 19,1% người tham gia nghiên cứu cho biết họ đã bảo quản bao cao su ở những chỗ không phù hợp với khuyến cáo.</p>

<p>Cách làm đúng: Nơi trữ bao cao su cần tránh trực tiếp dưới nắng quá lâu, tránh nơi có nhiệt độ cao (cốp xe chẳng hạn), nơi có ma sát nhiều (trong ví tiền). Theo khuyến cáo, tình trạng tiếp xúc với nắng, nhiệt, ma sát sẽ làm giảm chất lượng bao.</p>

